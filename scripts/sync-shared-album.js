#!/usr/bin/env node

/*
  Sync a public iCloud Shared Album or a public Google Photos shared link into
  config/photos.json.

  Usage:
    node scripts/sync-shared-album.js

  Reads config/album-source.json. If `enabled` is false, exits successfully
  without changes. Detects provider when `provider` is "auto".

  iCloud Shared Albums use Apple's public sharedstreams JSON API (the same one
  that the iCloud web viewer uses). The token is the part after the `#` in the
  share URL, e.g. https://www.icloud.com/sharedalbum/#B0xxxxxxxxx → B0xxxxxxxxx.

  Google Photos shared albums are best-effort scraping of the public HTML page.
  Google does not provide a stable public CDN/API for shared albums, so this
  may break without warning. Prefer iCloud for reliability.
*/

const fs = require("node:fs");
const path = require("node:path");

const ROOT = path.join(__dirname, "..");
const CONFIG_PATH = path.join(ROOT, "config", "album-source.json");

async function main() {
  const config = readJson(CONFIG_PATH);

  if (!config.enabled) {
    console.log("Album sync is disabled (config/album-source.json: enabled=false). Nothing to do.");
    return;
  }

  const url = (config.url || "").trim();
  if (!url) {
    fail("config/album-source.json: 'url' is empty. Paste a shared-album link.");
  }

  const provider = detectProvider(config.provider, url);
  console.log(`Syncing shared album using provider: ${provider}`);
  console.log(`Source URL: ${url}`);

  let photos;
  if (provider === "icloud") {
    photos = await syncICloud(url);
  } else if (provider === "google") {
    photos = await syncGooglePhotos(url);
  } else {
    fail(`Unknown provider: ${provider}. Use "icloud", "google", or "auto".`);
  }

  if (!photos || !photos.length) {
    fail("No media items were discovered in the shared album.");
  }

  const outputRel = config.output || "config/photos.json";
  const outputPath = path.join(ROOT, outputRel);

  const manifest = {
    source: {
      mode: "synced",
      provider,
      syncedAt: new Date().toISOString(),
      notes:
        "Generated by scripts/sync-shared-album.js. Edit config/album-source.json to change the source. Manual edits will be overwritten on the next sync."
    },
    photos
  };

  fs.writeFileSync(outputPath, `${JSON.stringify(manifest, null, 2)}\n`);
  console.log(`Wrote ${outputRel} with ${photos.length} media item(s).`);
}

function detectProvider(provider, url) {
  const normalized = (provider || "auto").toLowerCase();
  if (normalized === "icloud" || normalized === "google") return normalized;
  if (/icloud\.com\/sharedalbum/i.test(url)) return "icloud";
  if (/photos\.app\.goo\.gl|photos\.google\.com\/share/i.test(url)) return "google";
  fail(
    `Could not auto-detect provider from URL. Set "provider" to "icloud" or "google" in config/album-source.json.`
  );
  return "icloud";
}

/* ---------- iCloud ---------- */

function extractICloudToken(url) {
  const hash = url.split("#")[1];
  if (hash) return hash.split(/[?&/]/)[0];
  const tail = url.replace(/\/$/, "").split("/").pop();
  if (tail && /^[A-Za-z0-9]+$/.test(tail)) return tail;
  fail(`Could not extract iCloud share token from URL: ${url}`);
  return "";
}

function icloudPartition(token) {
  const first = token.charAt(0).toUpperCase();
  if (first === "A") return "01";
  const map = { B: "02", C: "03", D: "04", E: "05", F: "06", G: "07", H: "08", I: "09", J: "10" };
  return map[first] || "01";
}

async function icloudPost(host, token, endpoint, body) {
  const url = `https://${host}/${token}/sharedstreams/${endpoint}`;
  const response = await fetch(url, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      Accept: "application/json",
      Origin: "https://www.icloud.com",
      Referer: "https://www.icloud.com/"
    },
    body: JSON.stringify(body)
  });

  const redirectHost = response.headers.get("x-apple-mme-host");
  return { response, redirectHost };
}

async function syncICloud(url) {
  const token = extractICloudToken(url);
  let host = `p${icloudPartition(token)}-sharedstreams.icloud.com`;

  let result = await icloudPost(host, token, "webstream", { streamCtag: null });
  if (result.redirectHost) {
    host = result.redirectHost;
    result = await icloudPost(host, token, "webstream", { streamCtag: null });
  }
  if (!result.response.ok) {
    fail(`iCloud webstream request failed: HTTP ${result.response.status}. Album may be private or the link expired.`);
  }
  const stream = await result.response.json();
  const photoGuids = (stream.photos || []).map((p) => p.photoGuid).filter(Boolean);
  if (!photoGuids.length) {
    fail("iCloud album returned no photos. Check that the album is public and shared.");
  }

  const assetResult = await icloudPost(host, token, "webasseturls", { photoGuids });
  if (!assetResult.response.ok) {
    fail(`iCloud webasseturls request failed: HTTP ${assetResult.response.status}.`);
  }
  const assets = await assetResult.response.json();
  const items = assets.items || {};
  const locations = assets.locations || {};

  function buildItemUrl(checksum) {
    const item = items[checksum];
    if (!item) return null;
    const location = locations[item.url_location];
    if (!location || !location.hosts || !location.hosts.length) return null;
    return `${location.scheme || "https"}://${location.hosts[0]}${item.url_path}`;
  }

  const photos = [];
  let index = 0;
  for (const photo of stream.photos || []) {
    index += 1;
    const isVideo =
      Boolean(photo.mediaAssetType && /video/i.test(photo.mediaAssetType)) ||
      hasVideoDerivative(photo.derivatives);
    const chosen = pickBestDerivative(photo.derivatives, isVideo);
    if (!chosen || !chosen.checksum) continue;
    const mediaUrl = buildItemUrl(chosen.checksum);
    if (!mediaUrl) continue;
    photos.push({
      url: mediaUrl,
      alt: `Erinnerung ${index}`,
      type: isVideo ? "video" : "image"
    });
  }

  if (!photos.length) {
    fail("iCloud album was readable but no resolvable media URLs were produced.");
  }
  return photos;
}

function hasVideoDerivative(derivatives) {
  if (!derivatives) return false;
  for (const key of Object.keys(derivatives)) {
    const d = derivatives[key];
    if (d && typeof d.mediaType === "string" && /video|mp4|quicktime/i.test(d.mediaType)) return true;
  }
  return false;
}

function pickBestDerivative(derivatives, preferVideo) {
  if (!derivatives) return null;
  const entries = Object.entries(derivatives).map(([key, value]) => ({ key, ...value }));
  if (!entries.length) return null;
  if (preferVideo) {
    const videos = entries.filter((d) => d.mediaType && /video|mp4|quicktime/i.test(d.mediaType));
    if (videos.length) {
      videos.sort((a, b) => sizeOf(b) - sizeOf(a));
      return videos[0];
    }
  }
  entries.sort((a, b) => sizeOf(b) - sizeOf(a));
  return entries[0];
}

function sizeOf(d) {
  const w = parseInt(d.width, 10) || 0;
  const h = parseInt(d.height, 10) || 0;
  return w * h || parseInt(d.fileSize, 10) || 0;
}

/* ---------- Google Photos (best-effort) ---------- */

async function syncGooglePhotos(url) {
  console.warn(
    "Google Photos sync is best-effort scraping. Google does not provide a stable public API for shared albums; this may break."
  );
  let response;
  try {
    response = await fetch(url, {
      headers: {
        "User-Agent":
          "Mozilla/5.0 (X11; Linux x86_64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120 Safari/537.36",
        Accept: "text/html,application/xhtml+xml"
      },
      redirect: "follow"
    });
  } catch (error) {
    fail(`Google Photos fetch failed: ${error.message}`);
  }
  if (!response.ok) {
    fail(`Google Photos request failed: HTTP ${response.status}. The album may not be public.`);
  }
  const html = await response.text();

  // Public Google Photos shared albums embed an AF_initDataCallback for ds:1
  // that contains the ordered list of media items with their media-type marker
  // and optional duration. Parsing this gives us reliable image vs. video
  // detection and preserves album order. URL-pattern fallback is used only if
  // the structured payload is missing or unparseable.
  const items = parseGooglePhotosItems(html);
  if (items && items.length) {
    const photos = await buildGooglePhotosFromItems(items);
    if (photos.length) {
      const imageCount = photos.filter((p) => p.type === "image").length;
      const videoCount = photos.filter((p) => p.type === "video").length;
      console.log(
        `Google Photos: parsed ${photos.length} item(s) — ${imageCount} image(s), ${videoCount} video(s).`
      );
      if (videoCount === 0) {
        console.warn(
          "Google Photos: no playable video URLs were resolved. If the album contains videos, the share page may not expose them as MP4 — consider an iCloud public shared album for reliable video URLs."
        );
      }
      return photos;
    }
  }

  console.warn(
    "Google Photos: structured data parse failed, falling back to URL pattern scrape (videos cannot be detected this way)."
  );
  const urlPattern = /https:\/\/lh\d\.googleusercontent\.com\/[\w\-./=?]+/g;
  const found = html.match(urlPattern) || [];
  const seen = new Set();
  const photos = [];
  let index = 0;
  for (const raw of found) {
    const cleaned = raw.replace(/=[swh][\d-]+(?:[-\w]+)?$/, "");
    const finalUrl = `${cleaned}=s2048`;
    if (seen.has(finalUrl)) continue;
    seen.add(finalUrl);
    index += 1;
    photos.push({
      url: finalUrl,
      alt: `Erinnerung ${index}`,
      type: "image"
    });
  }

  if (!photos.length) {
    fail(
      "Google Photos scraping found no media. The page format may have changed, or the album is not public. " +
        "Consider switching to a public iCloud Shared Album for reliability."
    );
  }
  console.warn(
    `Google Photos fallback: scraped ${photos.length} image(s). Videos are not reliably detectable via fallback and are skipped.`
  );
  return photos;
}

// Live Photos / motion photos appear in the ds:1 payload looking very similar
// to real videos: they carry a non-image media-type marker and a short
// duration. Treating them as videos produces broken playback (the =dv URL
// either redirects to the still frame or to a short clip that renders
// poorly). We only trust an item as a real video when both:
//   1) the structured marker reads as video (14 — other non-1 markers tend
//      to be motion/live photos), AND
//   2) duration is at least LIVE_PHOTO_MAX_MS (real captured videos are
//      typically multiple seconds; Live Photos are ~1.5–3s clips bundled
//      with a still).
// Anything ambiguous is downgraded to image so the still frame renders
// correctly instead of a janky short clip.
const VIDEO_TYPE_MARKER = 14;
const LIVE_PHOTO_MAX_MS = 4000;

function parseGooglePhotosItems(html) {
  // The ds:1 init-data block contains an array of media items. Each item is
  // shaped roughly:
  //   [ id, [url, w, h, ..., [null, null, mediaTypeMarker], [fileSize], ...], timestamp, ..., { "146008172": [null, durationMs] } ]
  // mediaTypeMarker: 1 = image, 14 = video. Other markers (e.g. motion/live)
  // are present on Live Photos and short motion frames — those are NOT
  // playable videos and should be exported as still images.
  const start = html.indexOf("AF_initDataCallback({key: 'ds:1'");
  if (start < 0) return null;
  const dataIdx = html.indexOf("data:", start);
  if (dataIdx < 0) return null;
  const tail = html.slice(dataIdx + 5);
  const sideIdx = tail.indexOf(", sideChannel");
  if (sideIdx < 0) return null;
  const jsonText = tail.slice(0, sideIdx);
  let parsed;
  try {
    parsed = JSON.parse(jsonText);
  } catch (_error) {
    return null;
  }
  const items = Array.isArray(parsed) ? parsed[1] : null;
  if (!Array.isArray(items)) return null;
  const out = [];
  for (const entry of items) {
    if (!Array.isArray(entry)) continue;
    const meta = entry[1];
    if (!Array.isArray(meta) || typeof meta[0] !== "string") continue;
    const baseUrl = meta[0];
    if (!/^https:\/\/lh\d\.googleusercontent\.com\//.test(baseUrl)) continue;
    let typeMarker = null;
    for (let i = 2; i < meta.length; i += 1) {
      const v = meta[i];
      if (Array.isArray(v) && v.length === 3 && typeof v[2] === "number") {
        typeMarker = v[2];
        break;
      }
    }
    let durationMs = null;
    const extras = entry[entry.length - 1];
    if (extras && typeof extras === "object" && !Array.isArray(extras)) {
      const dur = extras["146008172"];
      if (Array.isArray(dur) && typeof dur[1] === "number") durationMs = dur[1];
    }
    const markerSaysVideo = typeMarker === VIDEO_TYPE_MARKER;
    const longEnough = typeof durationMs === "number" && durationMs >= LIVE_PHOTO_MAX_MS;
    const isVideo = markerSaysVideo && longEnough;
    const isLivePhoto =
      typeMarker !== null && typeMarker !== 1 && !isVideo;
    out.push({ baseUrl, isVideo, isLivePhoto, typeMarker, durationMs });
  }
  return out;
}

async function buildGooglePhotosFromItems(items) {
  const photos = [];
  let livePhotoCount = 0;
  let skippedVideoCount = 0;
  let index = 0;
  for (const item of items) {
    index += 1;
    const cleaned = item.baseUrl.replace(/=[swhmd][\w\d-]*$/, "");
    if (item.isVideo) {
      const videoUrl = `${cleaned}=dv`;
      const ok = await canFetchAsVideo(videoUrl);
      if (ok) {
        photos.push({
          url: videoUrl,
          alt: `Erinnerung ${index}`,
          type: "video"
        });
        continue;
      }
      skippedVideoCount += 1;
      console.warn(
        `Google Photos: item ${index} looked like a video (marker=${item.typeMarker}, duration=${item.durationMs}ms) but no playable URL could be resolved — skipping. Use iCloud sharing for reliable video URLs.`
      );
      continue;
    }
    if (item.isLivePhoto) {
      livePhotoCount += 1;
      console.log(
        `Google Photos: item ${index} is a Live Photo / motion frame (marker=${item.typeMarker}, duration=${item.durationMs}ms) — exporting still image only.`
      );
    }
    photos.push({
      url: `${cleaned}=s2048`,
      alt: `Erinnerung ${index}`,
      type: "image"
    });
  }
  if (livePhotoCount) {
    console.log(
      `Google Photos: ${livePhotoCount} Live Photo(s) emitted as still images (threshold: video marker=14 AND duration>=${LIVE_PHOTO_MAX_MS}ms).`
    );
  }
  if (skippedVideoCount) {
    console.log(
      `Google Photos: ${skippedVideoCount} ambiguous video item(s) skipped because no playable stream URL resolved.`
    );
  }
  return photos;
}

async function canFetchAsVideo(url) {
  try {
    const res = await fetch(url, {
      method: "GET",
      redirect: "manual",
      headers: {
        "User-Agent":
          "Mozilla/5.0 (X11; Linux x86_64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120 Safari/537.36"
      }
    });
    if (res.status >= 300 && res.status < 400) {
      const loc = res.headers.get("location") || "";
      return /videoplayback|video-downloads|googlevideo\.com/.test(loc);
    }
    if (res.ok) {
      const ct = (res.headers.get("content-type") || "").toLowerCase();
      return ct.startsWith("video/");
    }
    return false;
  } catch (_error) {
    return false;
  }
}

/* ---------- helpers ---------- */

function readJson(filePath) {
  try {
    return JSON.parse(fs.readFileSync(filePath, "utf8"));
  } catch (error) {
    fail(`Could not read ${filePath}: ${error.message}`);
  }
  return null;
}

function fail(message) {
  console.error(`sync-shared-album: ${message}`);
  process.exit(1);
}

main().catch((error) => {
  console.error(`sync-shared-album: unexpected error: ${error && error.message ? error.message : error}`);
  process.exit(1);
});
